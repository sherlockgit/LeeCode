package com.sherlock.utils;

/**
 * @Description: 两数相加链表
 * @Author: linmuyu
 * @Date: 2021/11/3 10:48
 */
public class ListNode {
  public int val;
  public ListNode next;
  public ListNode() {}
  public ListNode(int val) { this.val = val; }
  public ListNode(int val, ListNode next) { this.val = val; this.next = next; }
}
